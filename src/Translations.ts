// Helper function to get nested translations
export function getNestedTranslation(obj: any, path: string): string {
    // Handle interpolation for dynamic values like {{version}}
    const value = path.split('.').reduce((current, key) => current?.[key], obj);
    if (typeof value === 'string') {
        return value;
    }
    return path;
}

// Helper function to handle translations with parameters
export function t(language: 'en' | 'ka', key: string, params?: Record<string, any>): string {
    let translation = getNestedTranslation(translations[language], key);

    // Replace parameters in the translation string
    if (params) {
        Object.keys(params).forEach(param => {
            translation = translation.replace(`{{${param}}}`, params[param]);
        });
    }

    return translation;
}